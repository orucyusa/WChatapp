    class Read extends Thread{
        public void run(){
            while(true){
                try{
                    String m=din.readUTF();
                    if(m.contains(":;.,/=")){
                        m=m.substring(6);
                        dim.clear();
                        StringTokenizer st= new StringTokenizer(m,",");
                        while(st.hasMoreTokens()){
                            String u = st.nextToken();
                            if(!iD.equals(u)){
                                dim.addElement(u);
                            }
                        }
                    }
                    else{
                        msgBox.append(""+m+"\n");
                    }
                }
                catch(Exception ex){
                    break;
                }
            }
        }
    }

